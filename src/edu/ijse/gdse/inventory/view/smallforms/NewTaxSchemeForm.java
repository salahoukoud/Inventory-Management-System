    private void idComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_idComboItemStateChanged
        if (idCombo.getSelectedItem() != null) {
            try {
                String nextTaxId = TaxSchemeController.getNextTaxId();
                idText.setText(nextTaxId);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
            }

            TaxScheme selectedScheme = (TaxScheme) idCombo.getSelectedItem();
            categoryText.setText(selectedScheme.getTaxCategory());
            taxRateText.setText(Double.toString(selectedScheme.getTaxRate()));
            idText.setText(selectedScheme.getTaxId());
        }
    }//GEN-LAST:event_idComboItemStateChanged

    private TaxScheme getFormDetails() throws NumberFormatException {
        TaxScheme scheme = new TaxScheme();
        if (isAllFilled()) {
            scheme.setTaxId(idText.getText());
            scheme.setTaxCategory(categoryText.getText());
            scheme.setTaxRate(Double.parseDouble(taxRateText.getText()));
        }else{
            JOptionPane.showMessageDialog(NewTaxSchemeForm.this, "Please Fill All");
        }
        
        return scheme;
    }

