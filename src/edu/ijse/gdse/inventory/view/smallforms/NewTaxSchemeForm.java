    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        try {
            boolean isDeleted = TaxSchemeController.deleteTaxScheme(idText.getText());
            if (isDeleted) {
                setTaxIdCombo();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(NewTaxSchemeForm.this, "You Cannot Delete This Scheme");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void taxRateTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_taxRateTextKeyTyped
        isTextEntered = true;
        if(!addPressed){
            addButton.setText("Update");
        }
    }//GEN-LAST:event_taxRateTextKeyTyped

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try {
            if (!addPressed) {
                if (!isTextEntered) {
                    addButton.setText("Save");
                    setDefaults();
                    setNextTaxId();
                    idCombo.setEnabled(false);
                    addPressed = true;
                } else {
                    boolean isUpdated = TaxSchemeController.updateTaxScheme(getFormDetails());
                    if(isUpdated){
                        isTextEntered = false;
                        addButton.setText("Add New");
                        setDefaults();
                        setTaxIdCombo();
                    }
                }
            } else {
                boolean isAdded = TaxSchemeController.addNewTaxScheme(getFormDetails());
                if (isAdded) {
                    addButton.setText("Add New");
                    idCombo.setEnabled(true);
                    addPressed = false;
                    setTaxIdCombo();
                    setDefaults();
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

