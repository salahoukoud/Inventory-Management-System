    private void setTaxIdCombo() {
        idCombo.removeAllItems();
        idComboModel = (DefaultComboBoxModel) idCombo.getModel();
        try {
            ArrayList<TaxScheme> allTaxScehemes = TaxSchemeController.getAllTaxScehemes();
            for (TaxScheme scheme : allTaxScehemes) {
                idComboModel.addElement(scheme);
            }
            setDefaults();
        } catch (SQLException ex) {
            Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setDefaults() {
        idCombo.setSelectedIndex(-1);
        idText.setText(null);
        categoryText.setText(null);
        taxRateText.setText(null);
    }

    private void setNextTaxId() {
        try {
            String nextTaxId = TaxSchemeController.getNextTaxId();
            idText.setText(nextTaxId);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewTaxSchemeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private boolean isAllFilled(){
        return TextFieldHandler.isAllTextFieldsFilled(categoryText,taxRateText);
    }
    
