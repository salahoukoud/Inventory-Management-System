    private Vendor getSelectedVendorDetails() {
        //Get Data for create Vendor Object
        //Not Null fields
        String vendorId = vendorIdLabel.getText();
        String fName = fNameField.getText();
        String lName = lNameField.getText();
        String address1 = addressField1.getText();
        String address2 = addressField2.getText();
        String address3 = addressField3.getText();
        String address4 = addressField4.getText();
        PaymentTerm paymentTerm = (PaymentTerm) termsCombo.getSelectedItem();
        TaxScheme scheme = (TaxScheme) taxCombo.getSelectedItem();

        fullName = fName + " " + lName;
        fullAddress = address1 + "," + address2 + "," + address3 + "," + address4;
        int phoneNumber = Integer.parseInt(phoneNumberField.getText());
        String termId = paymentTerm.getTermId();
        double creditLimit = Double.parseDouble(creditLimitField.getText());
        String taxId = scheme.getTaxId();
        boolean isSelected = priceIncludeRadio.isSelected();
        
        //Null Accepted Fields
        String company = TextFieldHandler.textCheck(companyField.getText());
        String webSite = TextFieldHandler.textCheck(webSiteField.getText());
        int mobileNumber =TextFieldHandler.numberCheck(mobileNumberField.getText());
        String email = TextFieldHandler.textCheck(emailField.getText());
        int fax = TextFieldHandler.numberCheck(faxField.getText());
        
        
        //Create Vendor Object
        Vendor vendor = new Vendor();
        vendor.setVendorId(vendorId);
        vendor.setName(fullName);
        vendor.setAddress(fullAddress);
        vendor.setCompany(company);
        vendor.setTelephoneNumber(phoneNumber);
        vendor.setWebSite(webSite);
        vendor.setMobileNumber(mobileNumber);
        vendor.setEmailAddress(email);
        vendor.setFax(fax);
        vendor.setTermId(termId);
        vendor.setCreditLimit(creditLimit);
        vendor.setTaxStatus(isSelected);
        vendor.setTaxId(taxId);
        
        return vendor;
    }

