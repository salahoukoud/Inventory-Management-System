    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if (selectedVendor != null) {
            focusGainTextFields();
            
            vendorIdLabel.setText(selectedVendor.getVendorId());
            String[] name = selectedVendor.getName().split(" ");
            String[] address = selectedVendor.getAddress().split(",");
            
            if (name != null) {
                for (int i = 0; i < name.length; i++) {
                    nameField[i].setText(name[i]);
                }
            }
            
            if (address != null) {
                for (int i = 0; i < address.length; i++) {
                    addressField[i].setText(address[i]);
                }
            }
            phoneNumberField.setText(Integer.toString(selectedVendor.getTelephoneNumber()));

            for (int i = 2; i < termsCombo.getItemCount(); i++) {
                PaymentTerm term = (PaymentTerm) termsComboModel.getElementAt(i);
                if(term.getTermId().equals(selectedVendor.getTermId())){
                    termsCombo.setSelectedIndex(i);
                }
            }
            
            for (int i = 2; i < taxCombo.getItemCount(); i++) {
                TaxScheme tax = (TaxScheme) taxComboModel.getElementAt(i);
                if (tax.getTaxId().equals(selectedVendor.getTaxId())) {
                    taxCombo.setSelectedIndex(i);
                }
            }
            
            creditLimitField.setText(Double.toString(selectedVendor.getCreditLimit()));
            //Possbily null values
            if (selectedVendor.getCompany() != null) {
                companyField.setText(selectedVendor.getCompany());
            }
            if (selectedVendor.getWebSite() != null) {
                webSiteField.setText(selectedVendor.getWebSite());
            }
            if (selectedVendor.getEmailAddress() != null) {
                emailField.setText(selectedVendor.getEmailAddress());
            }
            if (selectedVendor.getMobileNumber() != 0) {
                mobileNumberField.setText(Integer.toString(selectedVendor.getMobileNumber()));
            }
            if (selectedVendor.getFax() != 0) {
                faxField.setText(Integer.toString(selectedVendor.getFax()));
            }

        }
    }//GEN-LAST:event_editButtonActionPerformed

