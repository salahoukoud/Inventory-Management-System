    private void searchComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_searchComboItemStateChanged
        
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (searchCombo.getSelectedItem() != null) {
                try {
                    String selectedItem = (String) searchCombo.getSelectedItem();
                    String[] split = selectedItem.split("-"); //Split from the '-' mark and get VID
                    String vid = null;
                    try{vid = split[1].trim();}catch(ArrayIndexOutOfBoundsException e){}
                    selectedVendor = VendorController.searchVendor(vid);
                    
                    if (selectedVendor != null) {
                        idLabel.setText(selectedVendor.getVendorId());
                        nameLabel.setText(selectedVendor.getName());
                        telephoneLabel.setText(Integer.toString(selectedVendor.getTelephoneNumber()));
                        companyLabel.setText(selectedVendor.getCompany());
                        creditLimitLabel.setText(Double.toString(selectedVendor.getCreditLimit()));
                    }
                    
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(NewVendorForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(NewVendorForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_searchComboItemStateChanged

